# Escape Sequence (\)

# How to have quotes withing a string:
weather = "It\'s \"kind of\" sunny"
print(weather)

# Say we want a backslash too:
weather = "It\\'s \"kind of\" sunny"
print(weather)

# There are other cool things we can do with this too.
# \t -> this adds a tab:
weather = "\tIt\'s \"kind of\" sunny"
print(weather)

# \n -> adds a new line:
weather = "\tIt\'s \"kind of\" sunny \n hope you have a good day!"
print(weather)