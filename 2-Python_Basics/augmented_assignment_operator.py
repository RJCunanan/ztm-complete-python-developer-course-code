# Augmented Assignment Operator

# Normally, we would do the following:
some_value = 5
some_value = some_value + 2

print(some_value)

# Instead of doing the above, we can do this:
some_value = 5
some_value += 2     # This is the augmented assignment operator

print(some_value)

some_value = 5
some_value -= 2     # This is the augmented assignment operator

print(some_value)

some_value = 5
some_value *= 2     # This is the augmented assignment operator

print(some_value)